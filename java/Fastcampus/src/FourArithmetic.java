// 클래스 단위로 코딩을 한다.
// 시작, 메인 클래스
// JavaSE 프로그램
public class FourArithmetic {
    // main() {} 메서드
    public static void main(String[] args) {
// Q. 두 개의 정수를 사칙연산(+,-,*,/)하여 출력하는 JavaSE프로그램을 만들어 보자.

        int a, b;
        a = 12;
        b = 2;

        System.out.println("a+b="+(a+b));
        System.out.println("a-b="+(a-b));
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));

    }
}
